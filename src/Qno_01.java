//1.Write a Java program to print the sum of two numbers, subtraction, multiplication, division

public class Qno_01 {
    public static void main(String args[])
    {
        int a = 12,b = 6;  //initializing two variables of a and b
        
        System.out.println("Sum of a and b - "+(a+b)); // Adding a and b
        System.out.println("Difference of a and b - "+(a-b)); // Subracting a and b
        System.out.println("Multiply a and b - "+(a*b)); // Multiplying a and b
        System.out.println("Divide a and b - "+(a/b)); // Dividing a and b
    }
}
